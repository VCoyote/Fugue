package notes.types;

/**
 * The ChordType represents what type of triad a chord is based off of.
 * 
 * @author Dan Pang
 *
 */
public enum ChordType {
	/**
	 * Major chord
	 */
	MAJOR,
	/**
	 * Minor chord
	 */
	MINOR,
	/**
	 * Augmented chord
	 */
	AUGMENTED,
	/**
	 * Diminished chord
	 */
	DIMINISHED
}
